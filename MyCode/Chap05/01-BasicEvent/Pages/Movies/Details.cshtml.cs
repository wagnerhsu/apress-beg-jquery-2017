using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using _01_BasicEvent.Data;
using _01_BasicEvent.Models;

namespace _01_BasicEvent.Pages.Movies
{
    public class DetailsModel : PageModel
    {
        private readonly _01_BasicEvent.Data._01_BasicEventContext _context;

        public DetailsModel(_01_BasicEvent.Data._01_BasicEventContext context)
        {
            _context = context;
        }

        public Movie Movie { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            Movie = await _context.Movie.FirstOrDefaultAsync(m => m.ID == id);

            if (Movie == null)
            {
                return NotFound();
            }
            return Page();
        }
    }
}
